def _doorbell_patent_element_keys(patent_idx: int) -> list:
    """Return which element keys are present for this doorbell patent.
    Designed so 2-3 features have < 30% HIGH coverage to trigger
    Low Coverage white-space findings with varied percentages.

    Feature coverage targets (out of 20 patents):
      camera:           20 (100%)  — SKIP
      face_recognition: 14 (70%)   — SKIP
      motion_sensor:    13 (65%)   — SKIP
      wifi:              8 (40%)   — SKIP
      lock_actuator:     4 (20%)   — Low Coverage finding
      notification:      5 (25%)   — Low Coverage finding
    """
    all_keys = ["camera", "face_recognition", "lock_actuator", "wifi", "notification", "motion_sensor"]
    if patent_idx < 3:
        # Patents 0-2: all 6 types  (lock_actuator ✓, notification ✓)
        return all_keys
    elif patent_idx < 4:
        # Patent 3: 5 types — notification ✓ but NO lock_actuator
        return ["camera", "face_recognition", "notification", "wifi", "motion_sensor"]
    elif patent_idx < 8:
        # Patents 4-7: camera, face_rec, motion, wifi  (no lock, no notif)
        return ["camera", "face_recognition", "motion_sensor", "wifi"]
    elif patent_idx < 12:
        # Patents 8-11: camera, face_rec, motion  (no lock, no notif, no wifi)
        return ["camera", "face_recognition", "motion_sensor"]
    elif patent_idx < 13:
        # Patent 12: camera, face_rec, motion, notification  (notification ✓)
        return ["camera", "face_recognition", "motion_sensor", "notification"]
    elif patent_idx < 15:
        # Patents 13-14: camera, motion_sensor  (minimal)
        return ["camera", "motion_sensor"]
    elif patent_idx < 18:
        # Patents 15-17: camera, face_recognition  (minimal)
        return ["camera", "face_recognition"]
    else:
        # Patents 18-19: camera, motion_sensor  (no notification — keeps total at 5)
        return ["camera", "motion_sensor"]


