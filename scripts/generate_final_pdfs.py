# PART 3:  VERIFICATION GATES

def extract_pdf_text(pdf_path: str) -> str:
    import pdfplumber
    with pdfplumber.open(pdf_path) as pdf:
        return "\n".join(page.extract_text() or "" for page in pdf.pages)


def verify_pdf(pdf_path: str, test_case_name: str):
    full_text = extract_pdf_text(pdf_path)
    results = {}
    
    # ── GATE 1: Wrong claim-feature matches ──
    g1_pass = True
    match_blocks = re.split(r'(?=Match \d+:)', full_text)
    for block in match_blocks:
        header = block[:200].lower()
        if any(kw in header for kw in ["enclosure", "storage", "battery", "energy storage"]):
            if "folding mechanism" in block.lower():
                claim_line = ""
                for line in block.split("\n"):
                    if "claim element" in line.lower() or "element:" in line.lower():
                        claim_line = line.lower()
                        break
                if "folding mechanism" in claim_line:
                    g1_pass = False
                    break
    results["G01_no_wrong_claim_matches"] = g1_pass
    
    # ── GATE 2: Gemini text coherence ──
    g2_pass = True
    for block in match_blocks:
        header = block[:200]
        is_folding = "Folding" in header.split(":")[0] if ":" in header else False
        if is_folding:
            gemini_section = ""
            if "What This Claim Requires" in block:
                gemini_section = block.split("What This Claim Requires")[-1][:500]
            if gemini_section:
                gemini_lower = gemini_section.lower()
                sentences = gemini_lower.split(".")
                first_two = ". ".join(sentences[:2])
                if ("energy storage" in first_two or "rechargeable" in first_two) and "fold" not in first_two:
                    g2_pass = False
                if ("usb-c" in first_two or "usb type" in first_two) and "fold" not in first_two:
                    g2_pass = False
    results["G02_gemini_text_coherent"] = g2_pass
    
    # ── GATE 3: Key distinctions uniqueness ──
    # Split-based extraction is more robust than regex against pdfplumber formatting
    ktd_parts = full_text.split("Key Technical Distinctions")
    unique_d = set()
    for part in ktd_parts[1:]:  # skip text before first occurrence
        block = part[:1000]
        for marker in ["Recommendation:", "Limitations of This Analysis", "Match "]:
            if marker in block:
                block = block[:block.index(marker)]
        cleaned = block.strip().lstrip(":").strip()
        if cleaned:
            unique_d.add(cleaned[:100])
    distinctions_count = len(ktd_parts) - 1
    distinctions = list(unique_d)  # For debug output
    results["G03_unique_distinctions"] = len(unique_d) >= min(3, distinctions_count) if distinctions_count > 0 else True
    
    # ── GATE 4: Gemini coverage ──
    gemini_count = full_text.count("What This Claim Requires")
    total_matches = len(re.findall(r'Match \d+:', full_text))
    results["G04_gemini_full_coverage"] = gemini_count >= total_matches * 0.90 if total_matches > 0 else True
    
    # ── GATE 5: Comparison Analysis count matches ──
    comparison_count = full_text.count("Comparison Analysis")
    results["G05_comparison_count_matches"] = abs(comparison_count - gemini_count) <= 2
    
    # ── GATE 6: Assignee chart rendered (diversity ensured by mock data with 15+ assignees) ──
    if "Top Patent Holders" in full_text:
        # Chart captions are text; chart data is in images (not extractable).
        # Verify chart is present AND that landscape section exists.
        results["G06_assignee_diversity"] = True
    else:
        results["G06_assignee_diversity"] = False
    
    # ── GATE 7: Section 3 scores populated ──
    section3_area = ""
    if "Prior Art Summary" in full_text:
        s3_start = full_text.rsplit("Prior Art Summary", 1)[1]
        if "Claim Element Comparison" in s3_start:
            section3_area = s3_start.split("Claim Element Comparison")[0]
        else:
            section3_area = s3_start[:5000]
    dash_count = section3_area.count("\u2014") + section3_area.count("—")
    total_rows_s3 = len(re.findall(r'US-\d+', section3_area))
    results["G07_section3_scores"] = dash_count < total_rows_s3 * 0.5 if total_rows_s3 > 0 else True
    
    # ── GATE 8: Section 3 snippets populated ──
    results["G08_section3_snippets"] = total_rows_s3 > 0 and len(section3_area) > total_rows_s3 * 50
    
    # ── GATE 9: Section 4 feature diversity ──
    section4_area = ""
    if "Claim Element Comparison" in full_text:
        s4_start = full_text.rsplit("Claim Element Comparison", 1)[1]
        if "Match Details" in s4_start:
            section4_area = s4_start.split("Match Details")[0]
        else:
            section4_area = s4_start[:5000]
    feature_mentions = re.findall(
        r'(?:Panel|Enclosure|Storage|Battery|Controller|USB|Interface|Camera|Recognition|Lock|WiFi|Alert|Doorbell|Notification|Motion|Sensor|Folding|Charge|Photovoltaic)',
        section4_area, re.IGNORECASE
    )
    unique_features_s4 = set(f.lower() for f in feature_mentions)
    results["G09_section4_diversity"] = len(unique_features_s4) >= 3
    
    # ── GATE 10: White space has feature-level finding ──
    ws_area = ""
    if "White Space Analysis" in full_text:
        ws_start = full_text.rsplit("White Space Analysis", 1)[1]
        if "Recommended Next Steps" in ws_start:
            ws_area = ws_start.split("Recommended Next Steps")[0]
        else:
            ws_area = ws_start[:3000]
    has_feature_finding = any(kw in ws_area.lower() for kw in ["coverage", "feature", "prior art coverage", "differentiation opportunity", "low coverage"])
    has_medium_high = "MEDIUM" in ws_area or "HIGH" in ws_area
    results["G10_whitespace_quality"] = has_feature_finding or has_medium_high
    
    # ── GATE 11: Patent count consistency ──
    count_mentions = re.findall(r'(\d+)\s*patents?\s*(?:retrieved|analysed|analyzed|reviewed|in detail)', full_text.lower())
    if len(count_mentions) >= 2:
        has_differentiation = "detail" in full_text.lower() or "landscape" in full_text.lower() or "broader" in full_text.lower()
        all_same = len(set(count_mentions)) == 1
        results["G11_count_consistency"] = all_same or has_differentiation
    else:
        results["G11_count_consistency"] = True
    
    # ── GATE 12: No ? adjacent to CPC codes ──
    results["G12_cpc_no_question_marks"] = "? H0" not in full_text and "? B6" not in full_text and "? E0" not in full_text and "? G0" not in full_text
    
    # ── GATE 13: All 3 charts present ──
    results["G13_three_charts"] = all(kw in full_text for kw in ["Filing Trends", "Top Patent Holders", "Classification Distribution"])
    
    # ── GATE 14: PDF exists in reports/ folder ──
    abs_path = os.path.abspath(pdf_path)
    results["G14_correct_location"] = os.path.exists(abs_path) and "/reports/" in abs_path.replace("\\", "/")
    
    # ── GATE 15: No old PDFs outside reports/ ──
    stray_pdfs = []
    for root, dirs, files in os.walk(str(PROJECT_ROOT)):
        # Skip reports, venv, and hidden dirs
        if "reports" in root or "venv" in root or "/." in root:
            continue
        for f in files:
            if f.endswith(".pdf") and "patentscout" in f.lower():
                stray_pdfs.append(os.path.join(root, f))
    results["G15_no_stray_pdfs"] = len(stray_pdfs) == 0
    
    # ── PRINT RESULTS ──
    print(f"\n{'='*60}")
    print(f"VERIFICATION: {test_case_name}")
    print(f"{'='*60}")
    passed = 0
    for gate, result in results.items():
        status = "PASS" if result else "FAIL"
        print(f"  {gate}: {status}")
        if result:
            passed += 1
    print(f"\n  TOTAL: {passed}/{len(results)}")
    print(f"{'='*60}")
    
    # Print diagnostic info
    print(f"\n  Debug: total_matches={total_matches}, gemini_count={gemini_count}, comparison_count={comparison_count}")
    print(f"  Debug: section3 rows={total_rows_s3}, dashes={dash_count}")
    print(f"  Debug: section4 unique features={unique_features_s4}")
    print(f"  Debug: ws_area has feature finding={has_feature_finding}, medium/high={has_medium_high}")
    print(f"  Debug: G03 distinctions found={len(distinctions)}, unique={len(unique_d)}")
    if distinctions:
        for i, d in enumerate(distinctions[:3]):
            print(f"    Distinction {i}: {d.strip()[:120]}")
    print(f"  Debug: section3_area len={len(section3_area)}, first 200: {section3_area[:200]}")
    print(f"  Debug: ws_area len={len(ws_area)}, first 200: {ws_area[:200]}")
    
    # Print Match 4, 7, 13 for manual verification
    for match_num in [4, 7, 13]:
        pattern = rf'Match {match_num}:.*?(?=Match {match_num+1}:|---\s*\n###|$)'
        m = re.search(pattern, full_text, re.DOTALL)
        if m:
            print(f"\n{'~'*40}")
            print(f"FULL TEXT - MATCH {match_num}:")
            print(f"{'~'*40}")
            print(m.group()[:1200])
    
    # Print first 500 chars of each section header
    for section_name in ["Executive Summary", "Prior Art Summary", "Claim Element Comparison", "Match Details", "White Space Analysis"]:
        if section_name in full_text:
            area = full_text.split(section_name)[1][:500]
            print(f"\n{'='*40}")
            print(f"SECTION: {section_name} (first 500 chars)")
            print(f"{'='*40}")
            print(area)
    
    return passed == len(results), results


