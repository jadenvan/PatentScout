def _solar_patent_element_keys(patent_idx: int) -> list:
    """Return which element keys are present for this patent index.
    Varies composition so not every patent has all 6 element types.
    Designed so 2-3 features have < 30% HIGH coverage to trigger
    Low Coverage white-space findings with varied percentages.

    Feature coverage targets (out of 20 patents):
      pv_array:  20 (100%)  — SKIP
      folding:   15 (75%)   — SKIP
      battery:   14 (70%)   — SKIP
      mppt:       8 (40%)   — SKIP
      enclosure:  4 (20%)   — Low Coverage finding
      usbc:       5 (25%)   — Low Coverage finding
    """
    all_keys = ["pv_array", "folding", "enclosure", "battery", "mppt", "usbc"]
    if patent_idx < 3:
        # Patents 0-2: all 6 types  (enclosure ✓, usbc ✓)
        return all_keys
    elif patent_idx < 4:
        # Patent 3: 5 types — usbc ✓ but NO enclosure
        return ["pv_array", "folding", "battery", "mppt", "usbc"]
    elif patent_idx < 8:
        # Patents 4-7: pv, folding, battery, mppt  (no enclosure, no usbc)
        return ["pv_array", "folding", "battery", "mppt"]
    elif patent_idx < 12:
        # Patents 8-11: pv, folding, battery  (no enclosure, no usbc, no mppt)
        return ["pv_array", "folding", "battery"]
    elif patent_idx < 13:
        # Patent 12: pv, folding, battery, usbc  (usbc ✓)
        return ["pv_array", "folding", "battery", "usbc"]
    elif patent_idx < 15:
        # Patents 13-14: pv, battery  (minimal)
        return ["pv_array", "battery"]
    elif patent_idx < 18:
        # Patents 15-17: pv, folding  (minimal)
        return ["pv_array", "folding"]
    else:
        # Patents 18-19: pv, battery  (no usbc — keeps usbc total at 5)
        return ["pv_array", "battery"]


def _build_solar_claim(patent_idx: int) -> str:
    """Build a realistic independent claim with varied elements per patent."""
    keys = _solar_patent_element_keys(patent_idx)
    elements = []
    for key in keys:
        variants = _SOLAR_ELEMENT_VARIANTS[key]
        elements.append(variants[patent_idx % len(variants)])
    claim = "1. A portable solar charging device comprising:\n"
    claim += ";\n".join(elements) + "."
    return claim


def _build_solar_parsed_claims(patent_idx: int, patent_number: str) -> dict:
    """Build a parsed_claims entry for a solar patent with varied elements."""
    keys = _solar_patent_element_keys(patent_idx)
    elements = []
    for eid, key in enumerate(keys):
        variants = _SOLAR_ELEMENT_VARIANTS[key]
        elements.append({
            "id": f"1{chr(97+eid)}",  # 1a, 1b, 1c, ...
            "text": variants[patent_idx % len(variants)],
        })
    return {
        "patent_number": patent_number,
        "total_claims_found": 20,
        "independent_claims": [{
            "claim_number": 1,
            "claim_type": "independent",
            "preamble": "A portable solar charging device",
            "transitional_phrase": "comprising",
            "elements": elements,
            "raw_text": _build_solar_claim(patent_idx),
            "full_text": _build_solar_claim(patent_idx),
        }],
        "parsing_method": "gemini",
        "parsing_confidence": "HIGH",
    }


def _build_solar_similarity_matches(detail_patents, parsed_claims, features):
    """Build diverse similarity matches covering all features, respecting per-patent element composition."""
    matches = []
    feature_element_map = {
        "Photovoltaic Panel Array": "pv_array",
        "Folding Panel Structure": "folding",
        "Compact Protective Enclosure": "enclosure",
        "Integrated Energy Storage Unit": "battery",
        "Universal Serial Bus Type-C Interface": "usbc",
        "Charge Controller Circuitry": "mppt",
    }
    
    # Score ranges per feature (to create diversity)
    base_scores = {
        "Photovoltaic Panel Array": 0.88,
        "Folding Panel Structure": 0.72,
        "Compact Protective Enclosure": 0.65,
        "Integrated Energy Storage Unit": 0.62,
        "Universal Serial Bus Type-C Interface": 0.65,
        "Charge Controller Circuitry": 0.65,
    }
    
    random.seed(42)  # reproducible
    
    for feat in features:
        fl = feat["label"]
        primary_key = feature_element_map.get(fl, "pv_array")
        base = base_scores.get(fl, 0.60)
        
        for pi, pat in enumerate(detail_patents[:20]):
            pn = pat["publication_number"]
            # Only match if patent has this element type
            patent_keys = _solar_patent_element_keys(pi)
            if primary_key not in patent_keys:
                continue
            
            variants = _SOLAR_ELEMENT_VARIANTS[primary_key]
            element_text = variants[pi % len(variants)]
            all_element_keys = ["pv_array", "folding", "enclosure", "battery", "mppt", "usbc"]
            eid_idx = patent_keys.index(primary_key)
            element_id = f"1{chr(97+eid_idx)}"
            
            score = base + random.uniform(-0.12, 0.08)
            score = max(0.30, min(0.95, round(score, 3)))
            
            if score >= 0.65:
                level = "HIGH"
            elif score >= 0.45:
                level = "MODERATE"
            else:
                level = "LOW"
            
            matches.append({
                "feature_label": fl,
                "feature_description": feat["description"],
                "feature_patent_language": feat.get("patent_language", ""),
                "element_text": element_text,
                "patent_number": pn,
                "claim_number": 1,
                "element_id": element_id,
                "similarity_score": score,
                "similarity_level": level,
                "patent_title": pat["title"],
                "patent_abstract": pat.get("abstract", ""),
            })
    
    # Sort by score desc
    matches.sort(key=lambda x: x["similarity_score"], reverse=True)
    
    # Stats
    high_count = sum(1 for m in matches if m["similarity_level"] == "HIGH")
    mod_count = sum(1 for m in matches if m["similarity_level"] == "MODERATE")
    low_count = sum(1 for m in matches if m["similarity_level"] == "LOW")
    
    return {
        "matches": matches,
        "unmatched_features": [],
        "stats": {
            "total_comparisons": high_count + mod_count + low_count,
            "high_matches": high_count,
            "moderate_matches": mod_count,
            "low_matches": low_count,
            "total_features": len(features),
            "total_patents": len(detail_patents),
        }
    }


